							<div class="small-12 medium-6 columns ab-left-side-content">
								<p class="ab-history-copy">What makes the Ferry Building San Francisco’s most famous landmark?  First is its strategic location at the foot of Market Street --- on the western edge of the continent, and at the center of the city’s financial, banking and transportation district.  Second is its history as the primary portal of the city.  Third, is the dramatic clock tower that has been the icon of the San Francisco waterfront for more than a 100 years.  <br />
									<br />
									Opening in 1898, the Ferry Building became the transportation focal point for anyone arriving by train. From the Gold Rush until the 1930s, arrival by ferryboat became the only way travelers and commuters – except those coming from the Peninsula – could reach the city. Passengers off the boats passed through an elegant two-story public area with repeating interior arches and overhead skylights.  At its peak, as many as 50,000 people a day commuted by ferry.</p>            </div>
							<div class="small-12 medium-6 columns ab-right-side-content">
								<p class="ab-history-copy">The opening of the Bay Bridge and Golden Gate Bridge, along with mass use of the automobile, rendered the daily commute by ferryboat obsolete. By the 1950’s, the Ferry Building was used very little. The historic interior of the Ferry Building structure was lost in 1955, when much of the building was converted to standard office space. The double-deck Embarcadero Freeway also cast its shadow for 35 years. until the 1989 Loma Prieta Earthquake.<br />
									<br />
									In March 2003, the landmark San Francisco Ferry Building reopened to the public after an extensive four-year restoration. The Ferry Building Marketplace -- a world class public food market -- is organized along a dramatic indoor street, the Nave. Today ferry terminals operate at Larkspur, Sausalito, Vallejo, and Alameda with plans for continuing network improvements and expansion.</p>            </div>
